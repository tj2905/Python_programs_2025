import math
#area of circle
r=float(input("enter the radius : "))
areaofcircle=math.pi*r*r
print("area of circle is",areaofcircle)
#area of square
s=float(input("enter the side of square : "))
areaofsquare=s*s
print("area of square is",areaofsquare)
#area of rectangle
l=float(input("enter the length of rectangle : "))
b=float(input("enter the breadth of rectangle : "))
areaofrectangle=l*b
print("area of rectangle is",areaofrectangle)
#area of triangle       
b=float(input("enter the base of triangle : "))
h=float(input("enter the height of triangle : "))
areaoftriangle=0.5*b*h
print("area of triangle is",areaoftriangle)